package info.longnetpro.common.util.generic;

public class NumberChineseConverterException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NumberChineseConverterException(Throwable throwable) {
		super(throwable);
	}

	public NumberChineseConverterException(String string, Throwable throwable) {
		super(string, throwable);
	}

	public NumberChineseConverterException(String string) {
		super(string);
	}

	public NumberChineseConverterException() {
		super();
	}
}
